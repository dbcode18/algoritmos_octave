function [L,U]=calcular_lu(A)
  U=[0, 0, 0; 0, 0, 0; 0, 0, 0];
  L=[1, 0, 0; 0, 1, 0; 0, 0, 1];
  
  U(1,1)=A(1,1);
  U(1,2)=A(1,2);
  U(1,3)=A(1,3);
  
  L(2,1)=A(2,1)/U(1,1);
  U(2,2)=A(2,2)-L(2,1)*U(1,2);
  U(2,3)=A(2,3)-L(2,1)*U(1,3);
  
  L(3,1)=A(3,1)/U(1,1);
  L(3,2)=(A(3,2)-L(3,1)*U(1,2))/U(2,2);
  U(3,3)=A(3,3)-L(3,1)*U(1,3)-L(3,2)*U(2,3);
  
  disp('U');
  disp(U);
  disp('L');
  disp(L);

endfunction
